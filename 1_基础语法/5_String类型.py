# '' 字符串

print('hello world')

# "" 字符串

print("hello world")

# + 字符串拼接

print('hello' + 'world')

# * 字符串重复

print('hello' * 2)

# 字符串截取

# 输出下标6到11的字符
print('hello world'[6:11])

# 输出下标1之后所有字符
print('hello world'[1:])

# 输出下标1至11后步长为2的字符
print('hello world'[1:11:2])

# 输出下标6之前倒数1位之间的字符
print('hello world'[6:-1])

# 输出下标6的字符
print('hello world'[6])

# \ 反斜杠转义

# \n 换行
print('hello\nworld')

# r 输出原始字符串
print(r'hello\nworld')