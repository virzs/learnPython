# 反斜杠换行

total = 'item_one' +\
    'item_two' +\
    'item_three'

print(total)

# []、{}、() 中不需要反斜杠

total = [
    'item_one',
    'item_two',
    'item_three'
]

print(total)

total = {
    'item_one': 1,
    'item_two': 2
}

print(total)

total = (
    'item_one' +
    'item_two' +
    'item_three'
)

print(total)
