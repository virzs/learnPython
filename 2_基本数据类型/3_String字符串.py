str = 'hello world'

# 输出字符串
print(str)

# 输出从第一个到倒数第二个中间的字符
print(str[0:-1])

# 输出第一个字符
print(str[0])

# 输出从第三个到第五个字符
print(str[2:5])

# 输出从第三个之后的字符
print(str[2:])

# 输出字符串两次
print(str * 2)

# 字符串拼接
print(str + 'python')

# \ 转义特殊字符
print('hello \n world')

# 输出原始字符
print(r'hello \n world')

# \ 续行
print('hello\
  world')

# 多行
print('''
  hello wolrd
  hello world
  hello wodrl
''')